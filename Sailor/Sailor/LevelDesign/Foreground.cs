        public Blok[,] blokArray = new Blok[30, 13];
        private List<Texture2D> texture { get; set; }

        public Foreground(List<Texture2D> texture)
        {
            this.texture = texture;
        }

        public void CreateWorld()
        {
            int shift = 0;
            Random r = new Random();
            for (int y = 0; y < tileArray.GetLength(0); y++)
            {
                shift = y % 2;
                for (int x = 0; x < tileArray.GetLength(1); x++)
                {
                    if (tileArray[y, x] == 1)
                    {
                        blokArray[y, x] = new Blok(texture[r.Next(0, texture.Count)], new Vector2((x * 64) - (shift * 32), y * 16));
                    }
                }
            }
        }

        public void DrawWorld(SpriteBatch spritebatch)
        {
            for (int x = 0; x < tileArray.GetLength(0); x++)
            {
                for (int y = 0; y < tileArray.GetLength(1); y++)
                {
                    if (blokArray[x, y] != null)
                    {
                        blokArray[x, y].Draw(spritebatch);
                    }
                }
            }

        }
